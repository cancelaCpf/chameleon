function yardToKilometer(number){
    return number / 1094
}

function yardToMeter(number){
    return number / 1.094
}

function yardToCentimeter(number){
    return number * 91.44
}

function yardToMillimeter(number){
    return number * 914
}

function yardToMicrometer(number){
    return number * 914400
}

function yardToNanometer(number){
    return number * 914400000000
}

function yardToMile(number){
    return number / 1760
}

function yardToFeet(number){
    return number * 3
}

function yardToInch(number){
    return number * 36
}

function yardToNauticalMile(number){
    return number / 2025
}