function nanometerToKilometer(number){
    return number / 1000000000000
}

function nanometerToMeter(number){
    return number / 1000000000
}

function nanometerToCentimeter(number){
    return number / 10000000
}

function nanometerToMillimeter(number){
    return number / 1000000
}

function nanometerToMicrometer(number){
    return number / 1000
}

