function feetToKilometer(number){
    return number / 3281
}

function feetToMeter(number){
    return number / 3.281
}

function feetToCentimeter(number){
    return number * 30.48
}

function feetToMillimeter(number){
    return number * 305
}

function feetToMicrometer(number){
    return number * 304800
}

function feetToNanometer(number){
    return number * 304800000000
}

function feetToMile(number){
    return number / 5280
}

function feetToYard(number){
    return number / 3
}

function feetToInch(number){
    return number * 12
}

function feetToNauticalMile(number){
    return number / 6076
}

