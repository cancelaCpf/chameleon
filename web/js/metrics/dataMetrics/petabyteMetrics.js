function petabyteToByte(number){
    return number * 1000000000000000
}

function petabyteToKilobyte(number){
    return number * 1000000000000
}

function petabyteToMegabyte(number){
    return number * 1000000000
}

function petabyteToGigabyte(number){
    return number * 1000000
}

function petabyteToTerabyte(number){
    return number * 1000
}

export default {
    petabyteToByte, 
    petabyteToKilobyte, 
    petabyteToMegabyte, 
    petabyteToGigabyte, 
    petabyteToTerabyte
}