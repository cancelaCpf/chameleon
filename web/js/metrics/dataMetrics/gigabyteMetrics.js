function gigabyteToByte(number){
    number * 1000000000
}

function gigabyteToKilobyte(number){
    return number * 1000000
}

function gigabyteToMegabyte(number){
    return number * 1000
}

function gigabyteToTerabyte(number){
    return number / 1000
}

function gigabyteToPetabyte(number){
    return number / 1000000
}