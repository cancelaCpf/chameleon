function bitToKilobit(number){
    return number / 1000
}

function bitToKibibit(number){
    return number / 1024
}

function bitToByte(number){
    return number / 8
}

function bitToKilobyte(number){
    return number / 8000
}

function bitToQuiloocteto(number){
    return (number / 8192).toFixed(7)
}

function bitToMegabyte(number){
    return number / 8000000
}

function bitToGigabyte(number){
    return number / 8000000000
}

function bitToTerabyte(number){
    return number / 8000000000000
}

function bitToPetabyte(number){
    return number / 8000000000000000
}